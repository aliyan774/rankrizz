document.addEventListener('DOMContentLoaded', function(){
    lottie.loadAnimation({
        container: document.getElementById('accordion-loader'), // Referencing the container by ID
        renderer: 'svg', // You can choose 'canvas' or 'html' as well, depending on your needs
        loop: true, // Loop the animation
        autoplay: true, // Autoplay on load
        path: 'assets/js/accordion-animation.json' // The path to your animation json
    });
});

